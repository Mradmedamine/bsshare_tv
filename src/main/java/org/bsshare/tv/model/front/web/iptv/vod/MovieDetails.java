package org.bsshare.tv.model.front.web.iptv.vod;

public class MovieDetails {

	private String vod_producer;
	private String vod_price;
	private String vod_country;
	private String vod_year;
	private String vod_duration;
	private String value;
	private String vod_description;
	private String caption;
	private String vod_actors;

	public String getVod_producer() {
		return vod_producer;
	}

	public void setVod_producer(String vod_producer) {
		this.vod_producer = vod_producer;
	}

	public String getVod_price() {
		return vod_price;
	}

	public void setVod_price(String vod_price) {
		this.vod_price = vod_price;
	}

	public String getVod_country() {
		return vod_country;
	}

	public void setVod_country(String vod_country) {
		this.vod_country = vod_country;
	}

	public String getVod_year() {
		return vod_year;
	}

	public void setVod_year(String vod_year) {
		this.vod_year = vod_year;
	}

	public String getVod_duration() {
		return vod_duration;
	}

	public void setVod_duration(String vod_duration) {
		this.vod_duration = vod_duration;
	}

	public String getValue() {
		return value;
	}

	public void setValue(String value) {
		this.value = value;
	}

	public String getVod_description() {
		return vod_description;
	}

	public void setVod_description(String vod_description) {
		this.vod_description = vod_description;
	}

	public String getCaption() {
		return caption;
	}

	public void setCaption(String caption) {
		this.caption = caption;
	}

	public String getVod_actors() {
		return vod_actors;
	}

	public void setVod_actors(String vod_actors) {
		this.vod_actors = vod_actors;
	}
}
